package sit.tu_varna.commands;

/**
 * Интерфейс, който дефинира командния модел за изпълнение на различни действия в системата.
 * Всеки клас, който имплементира този интерфейс, трябва да предоставя логика за изпълнение на конкретна команда.
 */
public interface Command {

    /**
     * Изпълнява командата с подадените аргументи.
     *
     * @param args аргументите, необходими за изпълнението на командата
     */
    void execute(String[] args);
}
